#pragma once

#include <algorithm>
#include <cctype>
#include <string>

namespace dlaf::util {
inline std::string copy_to_lower(std::string s) {
  std::transform(s.begin(), s.end(), s.begin(), [](unsigned char c) { return std::tolower(c); });
  return s;
}
}
